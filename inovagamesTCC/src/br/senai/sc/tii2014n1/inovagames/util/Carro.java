package br.senai.sc.tii2014n1.inovagames.util;

public class Carro {

	float valorCarro;
	int descontoCarro;
	float valorLiquido;
	float valorDesconto;
	
	
	public float getValorCarro() {
		return valorCarro;
	}
	public void setValorCarro(float valorCarro) {
		this.valorCarro = valorCarro;
	}
	public int getDescontoCarro() {
		return descontoCarro;
	}
	public void setDescontoCarro(int descontoCarro) {
		this.descontoCarro = descontoCarro;
	}
	public float getValorLiquido() {
		return valorLiquido;
	}
	public void setValorLiquido(float valorLiquido) {
		this.valorLiquido = valorLiquido;
	}
	public float getValorDesconto() {
		return valorDesconto;
	}
	public void setValorDesconto(float valorDesconto) {
		this.valorDesconto = valorDesconto;
	}
	
	
	
	
}
